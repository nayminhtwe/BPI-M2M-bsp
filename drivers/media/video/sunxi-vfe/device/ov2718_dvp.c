static int sensor_s_exp(struct v4l2_subdev *sd, unsigned int exp_val)
{
	unsigned char explow, exphigh;
	struct sensor_info *info = to_state(sd);

	if (exp_val > 0xffffff)
		exp_val = 0xfffff0;
	if (exp_val < 16)
		exp_val = 16;

	exp_val = (exp_val + 8) >> 4;	/*rounding to 1*/

	exphigh = (unsigned char)((0xff00 & exp_val) >> 8);
	explow = (unsigned char)((0x00ff & exp_val));

	sensor_write(sd, 0x30b7, explow);	/*coarse integration time*/
	sensor_write(sd, 0x30b6, exphigh);

	info->exp = exp_val;
	return 0;
}


static int sensor_g_gain(struct v4l2_subdev *sd, __s32 *value)
{
	struct sensor_info *info = to_state(sd);

	*value = info->gain;
	vfe_dev_dbg("sensor_get_gain = %d\n", info->gain);
	return 0;
}

static int sensor_s_gain(struct v4l2_subdev *sd, int gain_val)
{
	struct sensor_info *info = to_state(sd);
	unsigned char ana_gain = 0;
	unsigned int digi_gain = 0;

	if (gain_val > 128) {
		ana_gain = 0x43;
		digi_gain = gain_val << 1;
	} else if (gain_val > 64) {
		ana_gain = 0x42;
		digi_gain = gain_val << 2;
	} else if (gain_val > 32) {
		ana_gain = 0x41;
		digi_gain = gain_val << 3;
	} else {
		ana_gain = 0x40;
		digi_gain = gain_val << 4;
	}

	sensor_write(sd, 0x315b, (digi_gain & 0xff));
	sensor_write(sd, 0x315a, digi_gain >> 8);

	sensor_write(sd, 0x30bb, ana_gain);

	info->gain = gain_val;

	return 0;
}

static int ov2718_sensor_vts;
static int sensor_s_exp_gain(struct v4l2_subdev *sd,
			struct sensor_exp_gain *exp_gain)
{
	int exp_val, gain_val, shutter, frame_length;
	struct sensor_info *info = to_state(sd);

	exp_val = exp_gain->exp_val;
	gain_val = exp_gain->gain_val;

	if (gain_val < 1*16)
		gain_val = 16;
	if (gain_val > 64*16-1)
		gain_val = 64*16-1;

	if (exp_val > 0xfffff)
		exp_val = 0xfffff;

	shutter = exp_val/16;
	if (shutter > ov2718_sensor_vts - 4)
		frame_length = shutter + 4;
	else
		frame_length = ov2718_sensor_vts;

	sensor_s_exp(sd, exp_val);
	sensor_s_gain(sd, gain_val);

	info->exp = exp_val;
	info->gain = gain_val;
	return 0;
}


